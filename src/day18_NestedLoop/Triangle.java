package day18_NestedLoop;

public class Triangle {
    public static void main(String[] args) {

        String star = "* ";
        String a= "";

        for (int j = 0; j < 11; j++) {
            System.out.println(a+" ");;
            for (int i = 0; i < 1; i++) {
                a+= star;
            }
            System.out.println();
        }



    }
}

